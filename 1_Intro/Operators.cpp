#include <iostream>
using namespace std;

int main()
{
    // Arithmetic Operators
    cout << "Arithmeitc Operators:" << endl;

    int a = 10, b = 20;

    // Sum
    cout << "Sum: " << a + b << endl;

    // Difference
    cout << "Difference: " << a - b << endl;

    // Product
    cout << "Product: " << a * b << endl;

    // Quotient
    cout << "Quotient: " << a / b << endl;

    // Remainder
    cout << "Remainder: " << a % b << endl;

    // Relational Operators
    cout << "Relational Operators:" << endl;

    // Equal
    cout << "Equal: " << (a == b) << endl;

    // Not Equal
    cout << "Not Equal: " << (a != b) << endl;

    // Logical Operators

    cout << "Logical Operators:" << endl;

    // AND
    cout << "AND: " << (a && b) << endl;

    return 0;
}