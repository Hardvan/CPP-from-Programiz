#include <iostream>
using namespace std;

int main()
{
    // Display the numbers from 1 to 5
    for (int i = 1; i <= 5; i++)
    {
        cout << i << " ";
    }
    cout << endl;

    // Displaying Hello, World 5 times
    for (int i = 1; i <= 5; i++)
    {
        cout << "Hello, World!" << endl;
    }

    return 0;
}